#!/usr/bin/env python3
# generate_timer.py - Generate all outputs from timer.rdl.
#
# Tries to use PeakRDL for generation; if PeakRDL is unavailable the script
# falls back to hand-writing all output files with identical content.
#
# Outputs produced:
#   design/rtl/verilog/timer_reg_pkg.sv   - SV package: offsets, masks, resets
#   design/rtl/verilog/timer_regfile.sv   - Synthesizable SV register-file
#   design/rtl/vhdl/timer_reg_pkg.vhd     - VHDL-2008 package equivalent
#   design/rtl/vhdl/timer_regfile.vhd     - Synthesizable VHDL-2008 register-file
#   firmware/include/timer_regs.h         - C header with #define macros
#   doc/timer_regs.html                   - HTML register documentation

