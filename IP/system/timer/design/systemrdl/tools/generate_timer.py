# ---------------------------------------------------------------------------
# Environment guard
# ---------------------------------------------------------------------------
def _require_env():
    # type: () -> str
    timer_path = os.environ.get("CLAUDE_TIMER_PATH")
    if not timer_path:
        print("ERROR: CLAUDE_TIMER_PATH is not set.")
        print("       Please run:  source timer/setup.sh")
        sys.exit(1)
    return timer_path


