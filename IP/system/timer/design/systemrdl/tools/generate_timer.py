# STATUS field bit positions
STATUS_INTR_BIT   = 0
STATUS_ACTIVE_BIT = 1

# Reset values
CTRL_RESET   = 0x00000000
STATUS_RESET = 0x00000000
LOAD_RESET   = 0x00000000
COUNT_RESET  = 0x00000000


def _mask(msb, lsb):
    # type: (int, int) -> int
    """Return a bitmask covering bits [msb:lsb]."""
    width = msb - lsb + 1
    return ((1 << width) - 1) << lsb


