# Write helper
# ---------------------------------------------------------------------------
def _write_file(path, content):
    # type: (str, str) -> None
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="ascii", newline="\n") as fh:
        fh.write(content)
    print("  wrote " + path)


# ---------------------------------------------------------------------------
# PeakRDL path (attempted first)
# ---------------------------------------------------------------------------
def _try_peakrdl(rdl_file, outdir_sv, outdir_vhdl, outdir_c, outdir_html):
    # type: (str, str, str, str, str) -> bool
    """Attempt to generate outputs via PeakRDL. Returns True on success."""
    try:
        import peakrdl  # noqa: F401 - probe only
        from peakrdl.core import RDLCompiler  # noqa: F401
    except ImportError:
        return False

    try:
        import subprocess
        cmds = [
            ["peakrdl", "regblock", rdl_file, "-o", outdir_sv, "--type", "sv"],
            ["peakrdl", "regblock", rdl_file, "-o", outdir_vhdl, "--type", "vhdl"],
            ["peakrdl", "c-header", rdl_file, "-o", outdir_c],
            ["peakrdl", "html",     rdl_file, "-o", outdir_html],
        ]
        for cmd in cmds:
            result = subprocess.run(cmd, capture_output=True, text=True)
            if result.returncode != 0:
                print("WARNING: PeakRDL command failed: " + " ".join(cmd))
                print(result.stderr)
                return False
        return True
    except Exception as exc:  # noqa: BLE001
        print("WARNING: PeakRDL generation failed: " + str(exc))
        return False


