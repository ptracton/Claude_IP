# Main
# ---------------------------------------------------------------------------
def main():
    # type: () -> None
    timer_path = _require_env()

    parser = argparse.ArgumentParser(
        description="Generate timer outputs from SystemRDL source."
    )
    parser.add_argument(
        "--rdl",
        default=os.path.join(timer_path, "design", "systemrdl", "timer.rdl"),
        help="Path to the SystemRDL source file (default: %(default)s)",
    )
    parser.add_argument(
        "--outdir-sv",
        default=os.path.join(timer_path, "design", "rtl", "verilog"),
        help="Output directory for generated SystemVerilog (default: %(default)s)",
    )
    parser.add_argument(
        "--outdir-vhdl",
        default=os.path.join(timer_path, "design", "rtl", "vhdl"),
        help="Output directory for generated VHDL-2008 (default: %(default)s)",
    )
    parser.add_argument(
        "--outdir-c",
        default=os.path.join(timer_path, "firmware", "include"),
        help="Output directory for generated C header (default: %(default)s)",
    )
    parser.add_argument(
        "--outdir-html",
        default=os.path.join(timer_path, "doc"),
        help="Output directory for generated HTML documentation (default: %(default)s)",
    )
    args = parser.parse_args()

    print("generate_timer.py: generating all outputs ...")

    # Try PeakRDL first; fall back to hand-generation if unavailable.
    used_peakrdl = _try_peakrdl(
        args.rdl,
        args.outdir_sv,
        args.outdir_vhdl,
        args.outdir_c,
        args.outdir_html,
    )

    if used_peakrdl:
        print("  PeakRDL generation successful.")
    else:
        print("  PeakRDL not available - using hand-generation fallback.")
        _write_file(os.path.join(args.outdir_sv,   "timer_reg_pkg.sv"),  SV_PKG)
        _write_file(os.path.join(args.outdir_sv,   "timer_regfile.sv"),  SV_REGFILE)
        _write_file(os.path.join(args.outdir_vhdl, "timer_reg_pkg.vhd"), VHD_PKG)
        _write_file(os.path.join(args.outdir_vhdl, "timer_regfile.vhd"), VHD_REGFILE)
        _write_file(os.path.join(args.outdir_c,    "timer_regs.h"),      C_HEADER)
        _write_file(os.path.join(args.outdir_html, "timer_regs.html"),   HTML_DOC)

    print("generate_timer.py: done.")


if __name__ == "__main__":
    main()
